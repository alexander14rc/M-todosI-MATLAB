function p=RedlichKwong(x)
syms x
P=65000;
R=0.518;
Pc=4600;
Tc=191;
a=0.427*(R^2*(Tc^2.5)/Pc);
%a=0.427*(0.518^2*(191^2.5)/4600);
b=0.0866*(R*Tc/Pc);
%b=0.0866*(0.518*191/4600);
T=233;
p=-(R*T/(x-b))+(a/(x*(x+b)*sqrt(T)))+P;
%p=-(0.518*233/(x-0.0866*(0.518*191/4600)))+((0.427*(0.518^2*(191^2.5)/4600))/(x*(x+0.0866*(0.518*191/4600)))*sqrt(23300))+6500;
end